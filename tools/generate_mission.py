    """Update AI for enemy patrol units."""
    # Simple patrol AI
    if hasattr(unit, 'waypoints') and unit.waypoints:
        # Move towards next waypoint
        next_waypoint = unit.waypoints[0]
        distance = unit.position.distance(next_waypoint)

        if distance < 5:  # Reached waypoint
            unit.waypoints.pop(0)
            if not unit.waypoints:  # Loop the patrol
                # Reset waypoints (this would be the patrol route)
                unit.waypoints = unit.patrol_route[1:] + [unit.patrol_route[0]]
        else:
            # Move towards waypoint
            direction = (next_waypoint - unit.position).normalize()
            unit.position.x += direction.x * unit.speed * 0.016  # Assuming 60 FPS
            unit.position.y += direction.y * unit.speed * 0.016
            unit.position.z += direction.z * unit.speed * 0.016

