def generate_mission(mission_type: str = "combat_patrol", mission_name: str = "ParmaCombatPatrol"):
    """Generate a complete ArmA 3 mission."""

    # Create mission directory structure
    mission_dir = create_mission_structure(mission_name)

    # Generate Python mission code
    if mission_type == "combat_patrol":
        python_code = generate_combat_patrol_mission()
    else:
        python_code = generate_combat_patrol_mission()  # Default

    # Write Python code
    with open(mission_dir / "scripts" / "main.py", 'w') as f:
        f.write(python_code)

    # Transpile to SQF using Parma
    import subprocess
    import sys
    from pathlib import Path

    # Get the project root (parent of tools directory)
    project_root = Path(__file__).parent.parent
    sys.path.insert(0, str(project_root))

    # Ensure the Python file exists
    python_file = mission_dir / 'scripts' / 'main.py'
    if not python_file.exists():
        print(f"❌ Python file not found: {python_file}")
        return None

    result = subprocess.run([
        sys.executable, "-c",
        f"from src.parma.cli import main; import sys; sys.argv = ['parma', 'compile', '{python_file.resolve()}']; main()"
    ], capture_output=True, text=True, cwd=project_root)

    if result.returncode == 0:
        print(f"✅ Mission generated successfully: {mission_dir}")
        print("📁 Mission structure created")
        print("🐍 Python code written")
        print("⚡ SQF transpilation completed")

        # Create a README for the mission
        create_mission_readme(mission_dir, mission_type)

        return mission_dir
    else:
        print(f"❌ Failed to generate mission: {result.stderr}")
        return None

