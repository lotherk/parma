def mission_update(world, objectives, player):
    """Update mission state each frame."""
    # Update AI for enemy units
    for unit in world.units:
        if unit.faction == "enemy" and unit.health > 0:
            update_enemy_ai(unit, world)

    # Check mission completion
    if check_mission_completion(world, objectives):
        print("Mission accomplished!")
        return True  # Mission complete

    return False

