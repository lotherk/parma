    world.add_object(player)

    # Set up patrol routes
    routes = setup_patrol_routes()

    # Spawn enemy patrols
    for i, route in enumerate(routes):
        patrol_units = spawn_enemy_patrol(route, i)
        for unit in patrol_units:
            world.add_object(unit)

    # Set up objectives
    objectives = setup_objectives()

    print(f"Mission initialized: {len(world.units)} units, {len(routes)} patrols")
